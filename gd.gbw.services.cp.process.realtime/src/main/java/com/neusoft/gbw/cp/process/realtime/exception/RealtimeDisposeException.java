package com.neusoft.gbw.cp.process.realtime.exception;

public class RealtimeDisposeException extends Exception {

	private static final long serialVersionUID = 1L;
	
	public RealtimeDisposeException() {
		super();
	}

	public RealtimeDisposeException(String msg) {
		super(msg);
	}

	public RealtimeDisposeException(Throwable cause) {
		super(cause);
	}

	public RealtimeDisposeException(String msg, Throwable cause) {
		super(msg, cause);
	}
}

