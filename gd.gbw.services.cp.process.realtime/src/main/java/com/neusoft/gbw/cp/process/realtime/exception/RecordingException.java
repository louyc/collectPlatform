package com.neusoft.gbw.cp.process.realtime.exception;

public class RecordingException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 349259543284203885L;
	
	public RecordingException() {
		super();
	}

	public RecordingException(String msg) {
		super(msg);
	}

	public RecordingException(Throwable cause) {
		super(cause);
	}

	public RecordingException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
