package com.neusoft.gbw.cp.conver.exception;

public class NXmlException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 4641855060184993007L;

	public NXmlException() {
		super();
	}

	public NXmlException(String msg) {
		super(msg);
	}

	public NXmlException(Throwable cause) {
		super(cause);
	}

	public NXmlException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
