package com.neusoft.gbw.cp.process.inspect.exception;

public class InspectDisposeException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8611053024829629804L;

	public InspectDisposeException() {
		super();
	}

	public InspectDisposeException(String msg) {
		super(msg);
	}

	public InspectDisposeException(Throwable cause) {
		super(cause);
	}

	public InspectDisposeException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
