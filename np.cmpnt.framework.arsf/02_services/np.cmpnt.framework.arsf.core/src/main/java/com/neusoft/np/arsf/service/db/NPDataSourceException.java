package com.neusoft.np.arsf.service.db;

public class NPDataSourceException extends Exception {

	private static final long serialVersionUID = -8164868637198913999L;

	public NPDataSourceException() {
		super();
	}

	public NPDataSourceException(String msg) {
		super(msg);
	}

	public NPDataSourceException(Throwable cause) {
		super(cause);
	}

	public NPDataSourceException(String msg, Throwable cause) {
		super(msg, cause);
	}

}
