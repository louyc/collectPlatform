package com.neusoft.gbw.cp.process.measure.exception;

public class MeasureDisposeException extends Exception {

	private static final long serialVersionUID = 1L;
	
	public MeasureDisposeException() {
		super();
	}

	public MeasureDisposeException(String msg) {
		super(msg);
	}

	public MeasureDisposeException(Throwable cause) {
		super(cause);
	}

	public MeasureDisposeException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
